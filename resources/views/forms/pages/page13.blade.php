                    <p class="py-4">
                        <strong>*Guidelines regarding filling up of APAR with numerical grading*</strong>
                    </p>
                </div>

                <div>
                    <ul class="roman-list">
                        <li>
                            The columns in the APAR should be filled in with due care and attention and after devoting
                            adequate time.
                        </li>
                        <li>
                            It is expected that any grading of 1 or 2 (against work output or attributes or overall
                            grade) would be adequately justified with respect to specific accomplishments. Grades of 1-2
                            or 9-10 are expected to be rare occurrence sand hence the need to justify them. In awarding
                            a numerical grade the reporting and reviewing authorities should rate the Scientist against
                            a larger population of His/Her peers that may be currently working under them.
                        </li>
                        <li>
                            APARs graded between 8&10 will be rates as “Outstanding” and will be given a score of 9 for
                            the purpose of calculating average scores for Promotion/Up-gradation under the Scheme.
                        </li>
                        <li>
                            APARs graded between 6 and short of 8 will be rated as “Very Good” and will be given a score
                            of 7.
                        </li>
                        <li>
                            APARs graded between 4 and short of 6 will be rated as “Good” and will be given a score of
                            5.
                        </li>
                        <li>
                            APARs graded below 4 will be given a score of “Zero”.
                        </li>
                    </ul>
